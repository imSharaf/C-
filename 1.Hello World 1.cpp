#include<iostream>  // preprocessor directive --> compiler finds the defination of object in the function

int main(){
	
	std::cout<<"Hello World!\n"; //  \n marking new line
	std::cout<<"Hell World";
	
	return 0;
}

/*
<iostream> --> header file --> an standard c++ library
# --> indicates the 'include' as a preprocessor directive
<> --> indicates 'iostream'  library

main() --> function --> every c++ programme should have one --> used here to indicate the return type

std::cout --> standard output stream operator 
<< --> output operator
{} --> programme body
; --> ends the line programme
int --> return type of main function
*/
